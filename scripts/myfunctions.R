# FORMATS AND PRINTS THE CURRENT DATE

date2 <- function() {
  d <- date()
  month <- substr(d, 5, 7)
  day <- substr(d, 9, 10)  
  year <- substr(d, 21, 24)

  return(paste(month," ", day,", ",year, sep=""))
}





















